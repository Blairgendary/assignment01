				<h3>Hippodrome Heats Up a Chilly January Night</h3> 
				<h6>Wednesday, 17 January 2018 09:19 | Richard Amery</h6>
				<h4>Genre | R and B, Pop, Disco</h4>
                <img class= "hippodrome" src="./imgs/hippodrome.jpg">
                </div>
				<p>Hippodrome came ready to party and heat up  a cold January night, Jan. 6 at the Slice. I arrived in the middle of a hot, sweaty set by the local  R and B, pop, disco band and a floor full of enthusiastic dancers.

				They played a usual strong set full of toe tapping, booty shaking hits  including “That’s the Way I Like It,” and “the Beat Goes On.”

				Erica Hunt, Shawna Romolliwa and Juran Greene shared lead vocal duties, with Greene being the affable party host as always.


 				The band was tight as usual, with  the horn section and rhythm section all locked in.


 				Greene was enjoying himself to full measure, belting out “Mustang Sally,” and shone on “Soul Man.”


 				They showed their bluesy side with “Flip, Flop and Fly” and their disco side on songs like “Burn, Baby Burn.”</p>
